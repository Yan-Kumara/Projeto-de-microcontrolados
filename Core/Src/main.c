/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{


    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init();
    MX_TIM1_Init();

    while (1) {
        uint16_t freq = 0;  // Frequência inicial (silêncio)

        if (debounce(GPIOA, GPIO_PIN_1)) freq = notas[0];  // Dó (C4)
        if (debounce(GPIOA, GPIO_PIN_2)) freq = notas[1];  // Ré (D4)
        if (debounce(GPIOA, GPIO_PIN_3)) freq = notas[2];  // Mi (E4)
        if (debounce(GPIOA, GPIO_PIN_4)) freq = notas[3];  // Fá (F4)
        if (debounce(GPIOA, GPIO_PIN_5)) freq = notas[4];  // Sol (G4)
        if (debounce(GPIOA, GPIO_PIN_6)) freq = notas[5];  // Lá (A4)
        if (debounce(GPIOA, GPIO_PIN_7)) freq = notas[6];  // Si (B4)

        if (freq > 0) {
            set_PWM_frequency(freq);
            HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
        } else {
            HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_1);
        }

        HAL_Delay(10);  // Pequena pausa para evitar leituras muito rápidas
    }
}

