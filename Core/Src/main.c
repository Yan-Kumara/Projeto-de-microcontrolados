uint8_t debounce(GPIO_TypeDef *GPIOx, uint16_t GPIO_Pin) {
    if (HAL_GPIO_ReadPin(GPIOx, GPIO_Pin) == GPIO_PIN_SET) {  // Botão pressionado?
        HAL_Delay(10);  // Pequeno atraso para debounce
        if (HAL_GPIO_ReadPin(GPIOx, GPIO_Pin) == GPIO_PIN_SET) {  // Confirmação
            return 1;
        }
    }
    return 0;
}
