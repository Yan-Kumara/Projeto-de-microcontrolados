void set_PWM_frequency(uint16_t freq) {
    if (freq == 0) {
        __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 0);  // Desativa o PWM
    } else {
        uint32_t period = (50000000 / freq) - 1;  // Calcula ARR com APB1 = 50MHz
        __HAL_TIM_SET_AUTORELOAD(&htim1, period);
        __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, period / 2);  // 50% Duty Cycle
    }
}
